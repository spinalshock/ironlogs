const COLOR_MAP: Record<number, keyof typeof FACES['bloodlust']> = {
  1: 'skin',
  2: 'eye',
  3: 'mouth',
  4: 'highlight',
  5: 'shadow',
  6: 'extra',
};

export default function StatusFace({ level, size = 48 }: Props) {
  const face = FACES[level];
  const px = size / 16;

  const rects: ReactElement[] = [];
  for (let y = 0; y < 16; y++) {
    for (let x = 0; x < 16; x++) {
      const val = face.grid[y][x];
      if (val === 0) continue;
      const colorKey = COLOR_MAP[val];
      const color = colorKey ? (face[colorKey] as string) : face.skin;
      rects.push(
        <rect key={`${x}-${y}`} x={x * px} y={y * px} width={px} height={px} fill={color} />
      );
    }
  }

  return (
    <svg
      width={size}
      height={size}
      viewBox={`0 0 ${size} ${size}`}
      style={{ imageRendering: 'pixelated' }}
      aria-label={`Status: ${face.skin}`}
    >
      {rects}
    </svg>
  );
}
